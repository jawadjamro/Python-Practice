# Arithmetic operators
a = 10
b = 5

print(a + b)
print(a - b)
print(a * b)
print(a / b)
print(a % b)  # Reminder

# Relational operators
a = 50
b = 20

print(a == b)  # False
print(a != b)  # True
print(a >= b)  # True
print(a > b)   # True
print(a <= b)  # False
print(a < b)   # False

# Assignment operators
